
// Boolean values in JavaScript can be either true or false. They are often used in conditional statements to control the flow of the program.
// In JavaScript, certain values are considered "truthy" or "falsy" when evaluated in a boolean context.

/////////////////////////////////////////////////
// The following values are considered truthy:
/////////////////////////////////////////////////
/*
100 is true

3.14 is true

    - 15 is true

true is true

"Hello" is true

"false" is true

    (7 + 1 + 3.14) is true

    [] is true

{ } is true


In JavaScript, both an empty array [ ] and an empty object { } are truthy because they are objects.
All objects in JavaScript evaluate to true in a boolean context, regardless of their content.
*/


/////////////////////////////////////////////////
// In JavaScript, the following values are considered falsy:
/////////////////////////////////////////////////
/*
0 is false

"" is false

undefined is false

null is false

NaN is false

false is false

Comparing two JavaScript objects always returns false!
*/
