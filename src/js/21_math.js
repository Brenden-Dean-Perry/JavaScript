// Math
// Math is a built-in object in JavaScript that provides properties and methods for mathematical constants and functions.
// It is not a function object, so it cannot be called as a function, but it can be used as an object to access its properties and methods.


Math.abs(-12); // Output: 12 (absolute value of -12)

Math.ceil(4.2); // Output: 5 (rounds 4.2 up to the nearest integer)
Math.floor(4.7); // Output: 4 (rounds 4.7 down to the nearest integer)
Math.round(4.5); // Output: 5 (rounds 4.5 to the nearest integer)
Math.max(3, 7, 2); // Output: 7 (returns the largest of the given numbers)
Math.min(3, 7, 2); // Output: 2 (returns the smallest of the given numbers)
Math.pow(2, 3); // Output: 8 (2 raised to the power of 3)
Math.sqrt(16); // Output: 4 (square root of 16)
Math.random(); // Output: A random number between 0 (inclusive) and 1 (exclusive)
Math.PI; // Output: 3.141592653589793 (the value of π)
Math.E; // Output: 2.718281828459045 (the value of e)
